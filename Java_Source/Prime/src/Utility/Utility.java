package Utility;

import static java.lang.System.out;

public class Utility {
	//method over loading
	public static void print(int answer) {
		out.print(answer);
	}
	public static void print(float answer) {
		out.print(answer);
	}
	public static void print(double answer) {
		out.print(answer);
	}
	public static void print(char answer) {
		out.print(answer);
	}	
	public static void print(byte answer) {
		out.print(answer);
	}
	public static void print(long answer) {
		out.print(answer);
	}
	public static void print(boolean answer) {
		out.print(answer);
	}
	public static void print(String answer) {
		out.print(answer);
	}
	public static void print(String answer, Object ... obj) {
		
		out.printf(answer, obj);
	}
	
	
	public static void println(int answer) {
		out.print(answer);
	}
	public static void println(float answer) {
		out.print(answer);
	}
	public static void println(double answer) {
		out.print(answer);
	}
	public static void println(char answer) {
		out.print(answer);
	}	
	public static void println(byte answer) {
		out.print(answer);
	}
	public static void println(long answer) {
		out.print(answer);
	}
	public static void println(boolean answer) {
		out.print(answer);
	}
	public static void println(String answer) {
		out.print(answer);
	}
	public static void println(String answer, Object ... obj) {	
		out.printf(answer, obj);
	}
}
